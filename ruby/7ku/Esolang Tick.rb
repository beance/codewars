def interpreter(tape)
  dict = Hash.new(0)
  str = ""
  selector = 0

  tape.each_char do |char|
    case char
    when "+"
      dict[selector] = (dict[selector] + 1) % 256
    when ">"
      selector += 1
    when "<"
      selector -= 1
    when "*"
      str += dict[selector].chr
    end
  end

  str
end

